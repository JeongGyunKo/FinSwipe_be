# ── 1. SEC EDGAR 전체 티커 + 회사명 ──────────────────────────────
print("SEC EDGAR 데이터 수집 중...")
r = requests.get(
    "https://www.sec.gov/files/company_tickers.json",
    headers={"User-Agent": "FinSwipe swj0718820@gmail.com"},
    timeout=30,
)
sec_data = r.json()
sec_map = {}
for item in sec_data.values():
    t = item["ticker"].upper().strip()
    name = item["title"].strip()
    # 전부 대문자면 Title Case로 변환
    if name == name.upper():
        name = name.title()
    # 백슬래시 제거
    name = name.replace("\\", "")
    sec_map[t] = name
print(f"  SEC 티커 수: {len(sec_map)}")

