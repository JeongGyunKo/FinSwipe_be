# ── 4. 일반주식 필터 ──────────────────────────────────────────────
def is_common_stock(ticker: str) -> bool:
    if len(ticker) > 5:
        return False
    if re.search(r"[^A-Z.]", ticker):
        return False
    if ticker.endswith(("W", "WS", "WT", "WI", "R", "U", "Z")):
        return False
    return True


